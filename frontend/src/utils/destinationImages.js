// Function to get image for a destination
export const getDestinationImage = (destination) => {
  if (!destination) return destinationImages.default_landscape;
  
  // Clean the destination string
  const cleanDestination = destination.split(',')[0].trim();
  
  // Check for exact match
  if (destinationImages[cleanDestination]) {
    return destinationImages[cleanDestination];
  }
  
  // Check for partial matches
  const lowerDestination = cleanDestination.toLowerCase();
  
  // Check for mountain/hill destinations
  if (lowerDestination.includes('mountain') || lowerDestination.includes('hill') || 
      lowerDestination.includes('peak') || lowerDestination.includes('valley')) {
    return destinationImages.default_mountain;
  }
  
  // Check for beach/coastal destinations
  if (lowerDestination.includes('beach') || lowerDestination.includes('coast') || 
      lowerDestination.includes('sea') || lowerDestination.includes('ocean')) {
    return destinationImages.default_beach;
  }
  
  // Check for city-like destinations
  if (lowerDestination.includes('city') || lowerDestination.includes('town') || 
      lowerDestination.includes('metro')) {
    return destinationImages.default_city;
  }
  
  // Return default landscape for unknown destinations
  return destinationImages.default_landscape;
};

// Function to get a random image from the collection
export const getRandomDestinationImage = () => {
  const images = Object.values(destinationImages);
  return images[Math.floor(Math.random() * images.length)];
};

export default destinationImages;
